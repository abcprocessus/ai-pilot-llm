SOURCES = {
    "cot": (SYSTEM_COT, COT_PAIRS, "V_chain_of_thought"),
    "critic": (SYSTEM_CRITIC, CRITIC_PAIRS, "W_critic"),
    "marketplace": (SYSTEM_MARKETPLACE, MARKETPLACE_PAIRS, "X_marketplace"),
    "builder": (SYSTEM_BUILDER, BUILDER_PAIRS, "Y_builder"),
}


def generate_entries(sources_to_gen: list[str]) -> list[dict]:
    """Generate all entries for selected sources."""
    entries = []
    for src_name in sources_to_gen:
        if src_name not in SOURCES:
            print(f"WARNING: Unknown source '{src_name}', skipping")
            continue
        system, pairs, label = SOURCES[src_name]
        for user_msg, assistant_msg in pairs:
            entries.append(make_entry(system, user_msg, assistant_msg))
        print(f"  {label}: {len(pairs)} pairs")
    return entries


def main():
    parser = argparse.ArgumentParser(description="AI PILOT LLM — Advanced Sources Generator")
    parser.add_argument(
        "--output",
        default="datasets/advanced_sources.jsonl",
        help="Output JSONL file",
    )
    parser.add_argument(
        "--source",
        default="all",
        choices=["all", "cot", "critic", "marketplace", "builder"],
        help="Which source to generate (default: all)",
    )
    args = parser.parse_args()

    print("=" * 60)
    print("AI PILOT LLM — Advanced Sources Generator")
    print("=" * 60)

    if args.source == "all":
        sources_to_gen = list(SOURCES.keys())
    else:
        sources_to_gen = [args.source]

    entries = generate_entries(sources_to_gen)

    # Shuffle for diversity
    random.seed(42)
    random.shuffle(entries)

    # Write output
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        for entry in entries:
            f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    print(f"\n  Total: {len(entries)} pairs")
    print(f"  Output: {output_path}")
    print(f"  Size: {output_path.stat().st_size / 1024:.1f} KB")
    print("=" * 60)


if __name__ == "__main__":
    main()
