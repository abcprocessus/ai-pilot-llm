def main():
    parser = argparse.ArgumentParser(description="AI PILOT LLM Dataset Pipeline")
    parser.add_argument(
        "--constitutions-dir",
        default=os.environ.get("CONSTITUTIONS_DIR", "../../v2/backend/docs"),
        help="Path to CONSTITUTION_*.md files",
    )
    parser.add_argument(
        "--output-dir",
        default="datasets",
        help="Output directory for JSONL files",
    )
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    args = parser.parse_args()

    random.seed(args.seed)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    supabase_url = os.environ.get("SUPABASE_URL", "https://ckylppuqrsqfpezdusju.supabase.co")
    supabase_key = os.environ.get("SUPABASE_SERVICE_KEY", "")

    print("=" * 60)
    print("AI PILOT LLM — Dataset Pipeline")
    print("=" * 60)

    # Collect from all sources
    stats = {}

    print("\n[A] Constitutions...")
    constitutions = collect_constitutions(args.constitutions_dir)
    stats["A_constitutions"] = len(constitutions)

    if supabase_key:
        print("\n[B] Knowledge Base (Supabase)...")
        kb = collect_knowledge_base(supabase_url, supabase_key)
        stats["B_knowledge_base"] = len(kb)

        print("\n[C] Learning Log (Supabase)...")
        log = collect_learning_log(supabase_url, supabase_key)
        stats["C_learning_log"] = len(log)
    else:
        print("\n[B] WARNING: SUPABASE_SERVICE_KEY not set, skipping Knowledge Base")
        print("[C] WARNING: SUPABASE_SERVICE_KEY not set, skipping Learning Log")
        kb = []
        log = []
        stats["B_knowledge_base"] = 0
        stats["C_learning_log"] = 0

    print("\n[D] Code examples...")
    code = collect_code_examples()
    stats["D_code_examples"] = len(code)

    print("\n[E] Accounting examples...")
    accounting = collect_accounting_examples()
    stats["E_accounting"] = len(accounting)

    print("\n[F] Agent dialogs...")
    dialogs = collect_agent_dialogs()
    stats["F_agent_dialogs"] = len(dialogs)

    print("\n[G] General queries...")
    general = collect_general_queries()
    stats["G_general"] = len(general)

    print("\n[H] Domain expertise...")
    domain = collect_domain_expertise()
    stats["H_domain"] = len(domain)

    # Merge, deduplicate, and shuffle
    all_entries = constitutions + kb + log + code + accounting + dialogs + general + domain

    # Deduplication by (system_prompt + user_message) — different agents can share same questions
    seen_messages = set()
    unique_entries = []
    for entry in all_entries:
        system_msg = entry["messages"][0]["content"].strip().lower()[:50]
        user_msg = entry["messages"][1]["content"].strip().lower()
        key = f"{system_msg}||{user_msg}"
        if key not in seen_messages:
            seen_messages.add(key)
            unique_entries.append(entry)

    removed = len(all_entries) - len(unique_entries)
    if removed > 0:
        print(f"\n  Deduplication: removed {removed} duplicates")
    all_entries = unique_entries

    random.shuffle(all_entries)

    total = len(all_entries)
    stats["total"] = total
    stats["duplicates_removed"] = removed

    if total == 0:
        print("\nERROR: No training data collected!")
        sys.exit(1)

    # Split 90/10
    split_idx = int(total * 0.9)
    train = all_entries[:split_idx]
    val = all_entries[split_idx:]

    stats["train"] = len(train)
    stats["val"] = len(val)

    # Write JSONL
    train_path = output_dir / "train.jsonl"
    val_path = output_dir / "val.jsonl"
    stats_path = output_dir / "stats.json"

    with open(train_path, "w", encoding="utf-8") as f:
        for entry in train:
            f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    with open(val_path, "w", encoding="utf-8") as f:
        for entry in val:
            f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    with open(stats_path, "w", encoding="utf-8") as f:
        json.dump(stats, f, ensure_ascii=False, indent=2)

    print("\n" + "=" * 60)
    print(f"DONE: {total} total pairs")
    print(f"  Train: {len(train)} -> {train_path}")
    print(f"  Val:   {len(val)} -> {val_path}")
    print(f"  Stats: {stats_path}")
    print("=" * 60)


if __name__ == "__main__":
    main()
