def _fetch_paginated(url: str, headers: dict, page_size: int = 1000) -> list[dict]:
    """Fetch all rows from Supabase REST API with pagination."""
    import httpx
    all_rows = []
    offset = 0
    while True:
        sep = "&" if "?" in url else "?"
        paged_url = f"{url}{sep}limit={page_size}&offset={offset}"
        resp = httpx.get(paged_url, headers=headers, timeout=30)
        resp.raise_for_status()
        rows = resp.json()
        if not rows:
            break
        all_rows.extend(rows)
        if len(rows) < page_size:
            break
        offset += page_size
    return all_rows


def collect_knowledge_base(supabase_url: str, supabase_key: str) -> list[dict]:
    """Source B: Fetch agent_knowledge_base from Supabase."""
    try:
        import httpx
    except ImportError:
        print("  [B] WARNING: httpx not installed, skipping knowledge base")
        return []

    entries = []
    url = (
        f"{supabase_url}/rest/v1/agent_knowledge_base"
        "?select=agent_type,tags,content,confidence_score"
        "&is_active=eq.true"
        "&order=confidence_score.desc"
    )
    headers = {
        "apikey": supabase_key,
        "Authorization": f"Bearer {supabase_key}",
    }

    try:
        rows = _fetch_paginated(url, headers)
    except Exception as e:
        print(f"  [B] WARNING: failed to fetch knowledge base: {e}")
        return entries

    _kb_question_templates = [
        "Расскажи про {topic}",
        "Что ты знаешь про {topic}?",
        "Объясни мне {topic}",
        "Как работать с {topic}?",
        "Подробнее про {topic}",
        "В чём суть {topic}?",
        "Помоги разобраться с {topic}",
        "Какие правила для {topic}?",
    ]

    for idx, row in enumerate(rows):
        agent_type = row.get("agent_type", "general")
        agent_label = AGENT_NAMES.get(agent_type.upper(), f"AI агент ({agent_type})")
        system = f"Ты {agent_label} -- сотрудник AI PILOT."

        content = row.get("content", "").strip()
        if not content or len(content) < 30:
            continue

        tags = row.get("tags") or []
        if tags:
            topic = ", ".join(tags[:3])
        else:
            topic = agent_type

        # Use index-based template rotation to avoid duplicates
        tmpl = _kb_question_templates[idx % len(_kb_question_templates)]
        # Add content hash snippet to make each question truly unique
        snippet = content[:40].replace("\n", " ").strip()
        question = f"{tmpl.format(topic=topic)} [{snippet}]"

        entries.append(make_entry(system, question, content))

    print(f"  [B] Knowledge Base: {len(entries)} pairs from {len(rows)} rows")
    return entries


