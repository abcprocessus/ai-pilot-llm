# ---------------------------------------------------------------------------

AGENT_NAMES = {
    "LISA": "Лиза — AI Секретарь",
    "MARINA": "Марина — AI Менеджер по продажам",
    "IRYNA": "Ирина — AI Бухгалтер",
    "DANIIL": "Даниил — AI Специалист по рекламе",
    "KIRA": "Кира — AI Мастер соцсетей",
    "LEON": "Леон — AI Юрист",
    "VLAD": "Влад — AI Маркетолог",
    "ANNA": "Анна — AI HR-менеджер",
    "WEBMASTER": "Вебмастер — AI Веб-разработчик",
}

QUESTION_TEMPLATES = [
    "Расскажи о {topic}",
    "Что ты знаешь про {topic}?",
    "Как ты работаешь с {topic}?",
    "Объясни {topic}",
    "Какие правила для {topic}?",
    "Опиши свой подход к {topic}",
]


def _extract_agent_name(filename: str) -> Optional[str]:
    m = re.search(r"CONSTITUTION_(\w+)\.md", filename)
    if m:
        return m.group(1).upper()
    return None


def _split_sections(text: str) -> list[tuple[str, str]]:
    """Split markdown by ## headings, return (heading, body) pairs."""
    sections = []
    current_heading = ""
    current_body = []

    for line in text.split("\n"):
        if line.startswith("## "):
            if current_heading and current_body:
                body = "\n".join(current_body).strip()
                if len(body) > 50:
                    sections.append((current_heading, body))
            current_heading = line[3:].strip()
            current_body = []
        else:
            current_body.append(line)

    if current_heading and current_body:
        body = "\n".join(current_body).strip()
        if len(body) > 50:
            sections.append((current_heading, body))

    return sections


def collect_constitutions(constitutions_dir: str) -> list[dict]:
    """Source A: Parse constitution .md files into training pairs."""
    entries = []
    path = Path(constitutions_dir)

    if not path.exists():
        print(f"  [A] WARNING: constitutions dir not found: {constitutions_dir}")
        return entries

    md_files = sorted(path.glob("CONSTITUTION_*.md"))
    if not md_files:
        print(f"  [A] WARNING: no CONSTITUTION_*.md files in {constitutions_dir}")
        return entries

    for md_file in md_files:
        agent_key = _extract_agent_name(md_file.name)
        agent_label = AGENT_NAMES.get(agent_key, agent_key or "AI Агент")
        system = f"Ты {agent_label} — сотрудник AI PILOT."

        text = md_file.read_text(encoding="utf-8", errors="replace")
        sections = _split_sections(text)

        for heading, body in sections:
            # Generate 1-2 question variants per section
            topic = heading.lower().rstrip(".")
            template = random.choice(QUESTION_TEMPLATES)
            question = template.format(topic=topic)
            entries.append(make_entry(system, question, body))

            # Second variant for longer sections
            if len(body) > 200:
                template2 = random.choice(QUESTION_TEMPLATES)
                question2 = template2.format(topic=topic)
                if question2 != question:
                    entries.append(make_entry(system, question2, body))

    print(f"  [A] Constitutions: {len(entries)} pairs from {len(md_files)} files")
    return entries


