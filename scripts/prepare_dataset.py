def collect_learning_log(supabase_url: str, supabase_key: str) -> list[dict]:
    """Source C: Fetch high-quality interactions from agent_learning_log."""
    try:
        import httpx
    except ImportError:
        print("  [C] WARNING: httpx not installed, skipping learning log")
        return []

    entries = []
    url = (
        f"{supabase_url}/rest/v1/agent_learning_log"
        "?select=agent_type,user_message,ai_response,response_quality"
        "&response_quality=gte.4"
        "&order=created_at.desc"
        "&limit=5000"
    )
    headers = {
        "apikey": supabase_key,
        "Authorization": f"Bearer {supabase_key}",
    }

    try:
        resp = httpx.get(url, headers=headers, timeout=30)
        resp.raise_for_status()
        rows = resp.json()
    except Exception as e:
        print(f"  [C] WARNING: failed to fetch learning log: {e}")
        return entries

    for row in rows:
        user_msg = (row.get("user_message") or "").strip()
        ai_resp = (row.get("ai_response") or "").strip()
        if not user_msg or not ai_resp or len(ai_resp) < 30:
            continue

        agent_type = row.get("agent_type", "general")
        agent_label = AGENT_NAMES.get(agent_type.upper(), f"AI агент ({agent_type})")
        system = f"Ты {agent_label} — сотрудник AI PILOT."

        entries.append(make_entry(system, user_msg, ai_resp))

    print(f"  [C] Learning Log: {len(entries)} pairs from {len(rows)} rows")
    return entries


