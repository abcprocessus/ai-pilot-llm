# ---------------------------------------------------------------------------

def collect_knowledge_base(supabase_url: str, supabase_key: str) -> list[dict]:
    """Source B: Fetch agent_knowledge_base from Supabase."""
    try:
        import httpx
    except ImportError:
        print("  [B] WARNING: httpx not installed, skipping knowledge base")
        return []

    entries = []
    url = (
        f"{supabase_url}/rest/v1/agent_knowledge_base"
        "?select=agent_type,tags,content,confidence_score"
        "&is_active=eq.true"
        "&order=confidence_score.desc"
        "&limit=5000"
    )
    headers = {
        "apikey": supabase_key,
        "Authorization": f"Bearer {supabase_key}",
    }

    try:
        resp = httpx.get(url, headers=headers, timeout=30)
        resp.raise_for_status()
        rows = resp.json()
    except Exception as e:
        print(f"  [B] WARNING: failed to fetch knowledge base: {e}")
        return entries

    for row in rows:
        agent_type = row.get("agent_type", "general")
        agent_label = AGENT_NAMES.get(agent_type.upper(), f"AI агент ({agent_type})")
        system = f"Ты {agent_label} — сотрудник AI PILOT."

        content = row.get("content", "").strip()
        if not content or len(content) < 30:
            continue

        tags = row.get("tags") or []
        if tags:
            question = f"Расскажи про {', '.join(tags[:3])}"
        else:
            question = f"Что ты знаешь по теме {agent_type}?"

        entries.append(make_entry(system, question, content))

    print(f"  [B] Knowledge Base: {len(entries)} pairs from {len(rows)} rows")
    return entries


