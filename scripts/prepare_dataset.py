def collect_agent_dialogs() -> list[dict]:
    """Source F: Realistic agent conversation examples for all 9 agents.

    Generates:
    1. Base dialogs (hand-crafted)
    2. Rephrased variants (unique prefixes)
    3. Industry-specific templated dialogs (12 industries x 9 agents)
    """
    entries = []

    # 1. Base dialogs with rephrased variants
    for agent_key, dialogs in _AGENT_DIALOG_MAP.items():
        agent_label = AGENT_NAMES.get(agent_key, agent_key)
        system = f"Ты {agent_label} - сотрудник AI PILOT."

        for user_msg, assistant_msg in dialogs:
            entries.append(make_entry(system, user_msg, assistant_msg))

            # 2 unique rephrasings per dialog
            used_templates = set()
            for _ in range(2):
                tmpl = random.choice(_REPHRASE_TEMPLATES)
                while tmpl in used_templates:
                    tmpl = random.choice(_REPHRASE_TEMPLATES)
                used_templates.add(tmpl)
                q = user_msg.lower().rstrip("?.!,")
                rephrased = tmpl.format(q=q)
                entries.append(make_entry(system, rephrased, assistant_msg))

    # 2. Industry-specific templated dialogs
    for agent_key, templates in _AGENT_INDUSTRY_TEMPLATES.items():
        agent_label = AGENT_NAMES.get(agent_key, agent_key)
        system = f"Ты {agent_label} - сотрудник AI PILOT."

        for industry_data in _INDUSTRIES:
            industry = industry_data["name"]
            for q_template, a_template in templates:
                try:
                    question = q_template.format(industry=industry, **industry_data)
                    answer = a_template.format(industry=industry, **industry_data)
                    entries.append(make_entry(system, question, answer))
                except KeyError:
                    # Template uses keys not in industry_data — skip gracefully
                    pass

    print(f"  [F] Agent dialogs: {len(entries)} pairs ({len(_AGENT_DIALOG_MAP)} agents x {len(_INDUSTRIES)} industries)")
    return entries


