GENERATION_PROMPT = """Ты помогаешь создавать обучающий датасет для AI-модели.

Дисциплина: {discipline}
Тема: {topic}

Сгенерируй {n_pairs} РАЗНЫХ пар "вопрос-ответ" по этой теме.

Требования:
1. Вопросы от лица студента/ученика — реалистичные, конкретные
2. Включай разные уровни: школьный, университетский, продвинутый
3. Ответы подробные (200-600 слов), с примерами и структурой
4. Используй форматирование: **жирный**, списки, формулы, таблицы
5. Ответы на РУССКОМ языке
6. Для Беларуси/России — местный контекст где уместно (законы, учебники, стандарты)
7. Типы вопросов: объяснение понятия, решение задачи, подготовка к экзамену, помощь с курсовой/домашним заданием
8. НЕ придумывай ложную информацию — факты должны быть точными

Формат ответа — ТОЛЬКО JSON массив (без markdown, без пояснений):
[
  {{"question": "...", "answer": "..."}},
  {{"question": "...", "answer": "..."}}
]"""


async def generate_pairs(
    discipline: str,
    topic: str,
    n_pairs: int,
    api_key: str,
) -> list[dict]:
    """Use Claude Haiku to generate educational Q&A pairs."""
    import httpx

    prompt = GENERATION_PROMPT.format(
        discipline=discipline,
        topic=topic,
        n_pairs=n_pairs,
    )

    body = {
        "model": "claude-haiku-4-5-20251001",
        "max_tokens": 8192,
        "messages": [{"role": "user", "content": prompt}],
    }

    try:
        async with httpx.AsyncClient(timeout=120) as client:
            resp = await client.post(
                "https://api.anthropic.com/v1/messages",
                json=body,
                headers={
                    "x-api-key": api_key,
                    "anthropic-version": "2023-06-01",
                    "content-type": "application/json",
                },
            )
    except Exception as e:
        print(f"    NETWORK ERROR: {discipline}/{topic[:40]}: {e}", flush=True)
        raise  # re-raise — process_topic поймает и подождёт 30 сек

    if resp.status_code == 429:
        print(f"    RATE LIMIT 429: {discipline}/{topic[:40]}", flush=True)
        return []

    if resp.status_code != 200:
        print(f"    WARNING: Claude API {resp.status_code} for {discipline}/{topic[:40]}", flush=True)
        return []

    try:
        data = resp.json()
        text = data.get("content", [{}])[0].get("text", "")
    except Exception as e:
        print(f"    ERROR parsing response JSON: {e}", flush=True)
        return []

    print(f"DEBUG: raw text len={len(text)}, discipline={discipline}, topic={topic[:40]}", flush=True)

    pairs = _parse_pairs_ironclad(text)
    print(f"DEBUG: parsed={len(pairs)} pairs for {discipline}/{topic[:40]}", flush=True)
    return pairs


def _parse_pairs_ironclad(raw: str) -> list[dict]:
    """4-method ironclad JSON parser for Claude responses."""
    import re

    text = raw.strip()

    # ── Метод 1: clean JSON after stripping markdown fence ──────────────────
    try:
        clean = text
        if clean.startswith("```"):
            lines = clean.split("\n")
            start_i = 1
            end_i = len(lines)
            if lines[-1].strip() == "```":
                end_i = len(lines) - 1
            clean = "\n".join(lines[start_i:end_i]).strip()
        result = json.loads(clean)
        if isinstance(result, list) and result:
            return result
    except (json.JSONDecodeError, Exception):
        pass

    # ── Метод 2: обрезанный JSON — найти [, последнюю }, закрыть ] ──────────
    try:
        start = text.find("[")
        if start >= 0:
            fragment = text[start:]
            last_brace = fragment.rfind("}")
            if last_brace > 0:
                fragment = fragment[: last_brace + 1] + "]"
                result = json.loads(fragment)
                if isinstance(result, list) and result:
                    return result
    except (json.JSONDecodeError, Exception):
        pass

    # ── Метод 3: JSONDecoder.raw_decode() — каждый {} объект отдельно ───────
    salvaged: list[dict] = []
    decoder = json.JSONDecoder()
    idx = 0
    while idx < len(text):
        brace_pos = text.find("{", idx)
        if brace_pos == -1:
            break
        try:
            obj, end_pos = decoder.raw_decode(text, brace_pos)
            if isinstance(obj, dict) and "question" in obj and "answer" in obj:
                salvaged.append(obj)
            idx = brace_pos + end_pos
        except (json.JSONDecodeError, Exception):
            idx = brace_pos + 1

    if salvaged:
        return salvaged

    # ── Метод 4: regex fallback — собрать question/answer вручную ───────────
    pattern = re.compile(
        r'\{\s*"question"\s*:\s*"((?:[^"\\]|\\.)*)"\s*,\s*"answer"\s*:\s*"((?:[^"\\]|\\.)*)"\s*\}',
        re.DOTALL,
    )
    for m in pattern.finditer(text):
        try:
            obj = json.loads(m.group(0))
            salvaged.append(obj)
        except json.JSONDecodeError:
            try:
                q = m.group(1).replace('\\"', '"')
                a = m.group(2).replace('\\"', '"')
                if q and a:
                    salvaged.append({"question": q, "answer": a})
            except Exception:
                continue

    return salvaged


def make_entry(system: str, user: str, assistant: str) -> dict:
    return {
        "messages": [
            {"role": "system", "content": system.strip()},
            {"role": "user", "content": user.strip()},
            {"role": "assistant", "content": assistant.strip()},
        ]
    }


async def main():
    parser = argparse.ArgumentParser(description="AI PILOT LLM Educational Dataset Generator")
    parser.add_argument("--output", default="datasets/educational.jsonl", help="Output file")
    parser.add_argument("--pairs-per-topic", type=int, default=5, help="Q&A pairs per topic")
    parser.add_argument("--concurrency", type=int, default=2, help="Concurrent API calls")
    parser.add_argument("--dry-run", action="store_true", help="Show plan only")
    parser.add_argument("--disciplines", type=str, default="", help="Comma-separated list (empty=all)")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    parser.add_argument("--retry-failed", action="store_true",
                        help="Перезапуск только для тем без пар в существующем файле")
    args = parser.parse_args()

    random.seed(args.seed)

    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key and not args.dry_run:
        print("ERROR: ANTHROPIC_API_KEY not set")
        sys.exit(1)

    # Отбор дисциплин (если указано --disciplines)
    if args.disciplines:
        selected = [d.strip() for d in args.disciplines.split(",")]
        disciplines = {k: v for k, v in DISCIPLINES.items() if k in selected}
    else:
        disciplines = DISCIPLINES

    # --retry-failed: определяем какие темы уже покрыты
    covered_topics: set[str] = set()
    if args.retry_failed:
        output_path = Path(args.output)
        # Собираем темы из основного и partial файлов
        for fpath in [output_path, output_path.with_suffix(".partial.jsonl")]:
            if fpath.exists():
                with open(fpath, encoding="utf-8") as f:
                    for line in f:
                        line = line.strip()
                        if not line:
                            continue
                        try:
                            entry = json.loads(line)
                            # Извлекаем вопрос пользователя — он уникален для темы
                            user_q = entry["messages"][1]["content"].strip().lower()[:80]
                            covered_topics.add(user_q)
                        except (json.JSONDecodeError, KeyError, IndexError):
                            continue
        print(f"  [retry-failed] Уже покрыто вопросов: {len(covered_topics)}", flush=True)

    total_topics = sum(len(d["topics"]) for d in disciplines.values())
    total_expected = total_topics * args.pairs_per_topic

    print("=" * 60, flush=True)
    print("AI PILOT LLM — Educational Dataset Generator", flush=True)
    print("=" * 60, flush=True)
    print(f"\n  Disciplines: {len(disciplines)}", flush=True)
    print(f"  Topics: {total_topics}", flush=True)
    print(f"  Pairs per topic: {args.pairs_per_topic}", flush=True)
    print(f"  Expected output: ~{total_expected} pairs", flush=True)
    print(f"  Estimated cost: ~${total_topics * 0.002:.2f}", flush=True)
    if args.retry_failed:
        print(f"  Mode: RETRY FAILED (пропускаем покрытые темы)", flush=True)
    print(flush=True)

    for disc_name, disc_data in disciplines.items():
        print(f"  {disc_name}: {len(disc_data['topics'])} topics", flush=True)

    if args.dry_run:
        print("\n  --dry-run: stopping here", flush=True)
        return

    # Генерация
    semaphore = asyncio.Semaphore(args.concurrency)
    all_entries = []
    errors = 0
    completed = 0
    skipped = 0

    tasks_list = []
    for disc_name, disc_data in disciplines.items():
        for topic in disc_data["topics"]:
            tasks_list.append((disc_name, disc_data["system"], topic))

    random.shuffle(tasks_list)

    async def process_topic(disc_name: str, system: str, topic: str):
        nonlocal errors, completed, skipped
        async with semaphore:
            # --retry-failed: пропускаем темы с существующими парами
            if args.retry_failed and covered_topics:
                # Простая эвристика: тема уже покрыта если >2 пар с похожим вопросом
                topic_lower = topic.strip().lower()[:40]
                matching = sum(1 for ct in covered_topics if topic_lower in ct)
                if matching >= 2:
                    skipped += 1
                    return []

            # Retry — до 5 попыток; умные паузы
            max_retries = 5
            pairs = []
            for attempt in range(max_retries):
                try:
                    pairs = await generate_pairs(disc_name, topic, args.pairs_per_topic, api_key)
                    if pairs:
                        break
                    elif attempt < max_retries - 1:
                        wait = 15 * (attempt + 1)  # 15, 30, 45, 60 сек при пустом ответе
                        print(f"    EMPTY response ({disc_name}/{topic[:30]}), waiting {wait}s...", flush=True)
                        await asyncio.sleep(wait)
                except Exception as e:
                    err_str = str(e).lower()
                    if "429" in err_str or "rate" in err_str:
                        wait = 60 * (attempt + 1)
                    else:
                        wait = 30 * (attempt + 1)  # network error / disconnect
                    print(f"    ERROR attempt {attempt+1}/{max_retries} ({disc_name}/{topic[:30]}): {e} — waiting {wait}s", flush=True)
                    if attempt == max_retries - 1:
                        errors += 1
                        return []
                    await asyncio.sleep(wait)

            if not pairs:
                errors += 1
                print(f"    FAIL: {disc_name}/{topic[:40]} — 0 pairs after {max_retries} retries", flush=True)

            entries = []
            for pair in pairs:
                q = pair.get("question", "").strip()
                a = pair.get("answer", "").strip()
                if q and a and len(a) > 50:
                    entries.append(make_entry(system, q, a))

            all_entries.extend(entries)
            completed += 1
            if completed % 10 == 0:
                print(f"    [{completed}/{len(tasks_list)}] {len(all_entries)} pairs so far", flush=True)
            # Промежуточное сохранение каждые 50 тем
            if completed % 50 == 0 and all_entries:
                _tmp = Path(args.output).with_suffix(".partial.jsonl")
                with open(_tmp, "w", encoding="utf-8") as _f:
                    for _e in all_entries:
                        _f.write(json.dumps(_e, ensure_ascii=False) + "\n")
                print(f"    [checkpoint] saved {len(all_entries)} to {_tmp}", flush=True)

            await asyncio.sleep(0.3)
            return entries

    await asyncio.gather(
        *[process_topic(d, s, t) for d, s, t in tasks_list]
    )

    print(f"\n  Total collected: {len(all_entries)} entries", flush=True)

    # Deduplicate
    seen = set()
    unique = []
    for entry in all_entries:
        key = entry["messages"][1]["content"].strip().lower()[:100]
        if key not in seen:
            seen.add(key)
            unique.append(entry)

    removed = len(all_entries) - len(unique)
    print(f"\n  Generated: {len(unique)} unique pairs ({removed} duplicates removed, {errors} errors, {skipped} skipped)", flush=True)

    # Сохранение
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # При --retry-failed дописываем в конец файла
    mode = "a" if args.retry_failed else "w"
    with open(output_path, mode, encoding="utf-8") as f:
        for entry in unique:
            f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    action = "Appended to" if args.retry_failed else "Saved to"
    print(f"  {action} {output_path}", flush=True)
    print(f"\n  To merge: python scripts/merge_datasets.py", flush=True)
    print("=" * 60, flush=True)


if __name__ == "__main__":
    asyncio.run(main())
